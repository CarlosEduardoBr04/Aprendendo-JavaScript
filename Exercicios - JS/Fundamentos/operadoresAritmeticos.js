// Destructuring assignment para atribuir os valores do array [3, 5, 6, 9] às variáveis 'a', 'b', 'c' e 'd'
const [a, b, c, d] = [3, 5, 6, 9];

// Calcula a soma dos valores de 'a', 'b', 'c' e 'd'
const soma = a + b + c + d;

// Calcula a subtração de 'b' de 'd'
const subtracao = d - b;

// Calcula a multiplicação de 'a' por 6
const mult = a * 6;

// Calcula a divisão de 'b' por 'a'
const div = b / a;

// Calcula o módulo (resto) da divisão de 'a' por 2
const modulo = a % 2;

// Imprime os resultados das operações no console
console.log(soma, subtracao, mult, div, modulo);
