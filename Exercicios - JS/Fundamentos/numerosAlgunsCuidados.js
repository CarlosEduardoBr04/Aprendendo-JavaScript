console.log(7 / 0); // Imprime Infinity, pois dividir por zero resulta em infinito em JavaScript
console.log("10" / 2); // Converte a string "10" para número e realiza a divisão, resultando em 5
console.log("Carro" * 2); // Não é possível multiplicar uma string por um número, resulta em NaN (Not a Number)
console.log(0.1 + 0.7); // Resulta em 0.7999999999999999 devido à imprecisão de representação de ponto flutuante
