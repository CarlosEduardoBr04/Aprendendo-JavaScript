// 1. Cria uma constante 'a' com o valor 7.
const a = 7 

// 2. Cria uma variável 'b' com o valor 3.
let b = 3

// 3. Atribui à variável 'b' o valor dela mesma mais o valor da constante 'a'. (b = b + a)
b += a
console.log(b) // Saída: 10

// 4. Subtrai 4 da variável 'b'. (b = b - 4)
b -= 4
console.log(b) // Saída: 6

// 5. Multiplica a variável 'b' por 2. (b = b * 2)
b *= 2
console.log(b) // Saída: 12

// 6. Divide a variável 'b' por 2. (b = b / 2)
b /= 2
console.log(b) // Saída: 6

// 7. Calcula o resto da divisão da variável 'b' por 2. (b = b % 2)
b %= 2
console.log(b) // Saída: 0
