// Função

// A chamada 'console.log(typeof Object)' exibe o tipo da função Object, que é uma função construtora em JavaScript.
console.log(typeof Object);

// A declaração 'class Produto {}' cria uma classe chamada Produto.
// 'console.log(typeof Produto)' exibe o tipo da classe, que é uma função construtora.
console.log(typeof Produto);
